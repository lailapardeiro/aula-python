# Utilização de Operadores Relacionais
a = 5 
b = 3 

# Igualdade
resultado = a == b
print(resultado)

# Diferença
resultado = a != b
print(resultado)

# Maior que (>)
resultado = a > b
print(resultado)

# Menor que (<)
resultado = a < b
print(resultado)

# Maior ou igual (>=)
resultado = a >= b
print(resultado)

# Menor ou igual (<=)
resultado = a <= b 
print(resultado)

