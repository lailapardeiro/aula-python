a = 5
b = 3 
c = 4

# Adição (+)
resultado = a + b + c
print(f'A soma é de: {resultado}')

# Subtração (-)
resultado = c - b
print(f'A subtração é de: {resultado}')

# Multiplicação (*)
resultado = b * a 
print(f'A multiplicação é de: {resultado}')

# Divisão (/)
resultado = a / b 
print(f'A divisão é de: {resultado}')

# Divisão Inteira (//)
resultado = b // a
print(f'A divisão inteira é de: {resultado}')

# Resto da Divisão (%)
resultado = a % b
print(f'O resto da divisão é de: {resultado}')

# Exponêncial (**)
resultado = a ** b 
print(f'O resultado da exponênciação é de: {resultado}')


